// Faça um Programa que verifique se 
// uma letra digitada é vogal ou consoante.

function vowelOrConsoant(ch) {
    if (ch !=='a'&& ch !== 'e' && ch !=='i' && ch !== 'o'&& ch !== 'u') {
        console.log("consoante")
    }
    else {
        console.log("vogal")
    }    
}

vowelOrConsoant('c')
console.log(vowelOrConsoant)